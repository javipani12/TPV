    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        // Guardamos el valor del password
        String valor = jTextFieldPassword.getText();
        // Si su longitud es diferente de 0, hacemos una subcadena quitando
        // el último carácter
        if (valor.length() != 0) {
            valor = valor.substring(0, valor.length() - 1);
            jTextFieldPassword.setText(valor);
        }
    }//GEN-LAST:event_jButtonBorrarActionPerformed

    private void jButtonValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValidarActionPerformed
        // Guardamos los valores del login y de la contraseña
        String login = jTextFieldLogin.getText();
        String password = jTextFieldPassword.getText();

        // Comprobamos si el usuario existe
        if (conexion.comprobarUsuario(login, Integer.parseInt(password))) {
            
            // Creamos el nuevo usuario y obtenemos los productos de la BBDD
            // para crear un objeto Venta
            Usuario user = conexion.obtenerUsuario(login, Integer.parseInt(password));
            Venta venta = new Venta(user);
            VentanaProductos ventanaProductos = new VentanaProductos(venta);
            JOptionPane.showMessageDialog( null,
                    "Te has logueado como " + login);
            this.setVisible(false);
            ventanaProductos.setVisible(true);
        } else {
            JOptionPane.showMessageDialog( null,
            "Los datos introducidos no coinciden en la Base de Datos");
        }
        
    }//GEN-LAST:event_jButtonValidarActionPerformed

