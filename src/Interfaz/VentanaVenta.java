    private void jButtonRealizarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRealizarVentaActionPerformed
        long codigo = venta.generaCodigo();
        for (int i = 0; i < venta.getProductos().size(); i++) {
            conexion.insertarVenta(i, venta, codigo);
            int stockActual = venta.getProductos().obtenerListaProductos().get(i).getStock();
            venta.getProductos().obtenerListaProductos().get(i).setStock(stockActual - venta.getUnidades().get(i));
            stockActual = venta.getProductos().obtenerListaProductos().get(i).getStock();
            conexion.actualizarStock(stockActual, venta.getProductos().obtenerListaProductos().get(i).getCodProducto());
        }
        venta.reiniciarVenta();
        ventanaProductos.borrarCuenta();
        this.dispose();
    }//GEN-LAST:event_jButtonRealizarVentaActionPerformed

