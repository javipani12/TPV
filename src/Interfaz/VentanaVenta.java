    public void mostrarProductos(){
        for (int i = 0; i < venta.getProductos().size(); i++) {
            jTextAreaDesglose.append(venta.getProductos().obtenerProductoPorPosicion(i).getNombre()+" x");
            jTextAreaDesglose.append(venta.getUnidades().get(i).toString()+"\n");
       }
    }
    
    public void mostrarFechaHora(){
        LocalDateTime fechaHora = LocalDateTime.now();
        int anno = fechaHora.getYear();
        int mes = fechaHora.getMonthValue();
        int dia = fechaHora.getDayOfMonth();
        int horas = fechaHora.getHour();
        int minutos = fechaHora.getMinute();
        int segundos = fechaHora.getSecond();
        jLabelFecha.setText(dia+"/"+mes+"/"+anno); 
        jLabelHora.setText(horas+":"+minutos+":"+segundos);
    }


